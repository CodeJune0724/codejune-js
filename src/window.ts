export default {

    /**
     * 获取屏幕高度
     *
     * @return 屏幕高度
     * */
    getScreenHeight(): number {
        return window.screen.height;
    },

    /**
     * 获取屏幕宽度
     *
     * @return 屏幕宽度
     * */
    getScreenWidth(): number {
        return window.screen.width;
    }

};